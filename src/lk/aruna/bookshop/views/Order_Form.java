    private void generateOrderID() throws ClassNotFoundException, SQLException {
       
            String orderId = OrderController.generateOID();
            
            if(orderId !=null){
            String[] orarr = orderId.split("B");
            int ornumber = Integer.parseInt(orarr[1]);
            ornumber +=1;
            
                if(ornumber < 10){
                txt_OrderID.setText("B00"+ornumber);
                }else if(ornumber < 100){
                txt_OrderID.setText("B0"+ornumber);
                }else{
                txt_OrderID.setText("B"+ornumber);
                }

            }else{
            txt_OrderID.setText("B001");
            }
        
    }

    private void generateCustID() throws ClassNotFoundException, SQLException {
        String custId = OrderController.generateCID();
        
            if(custId !=null){
            String[] cuarr = custId.split("C");
            int cunumber = Integer.parseInt(cuarr[1]);
            cunumber +=1;
            
                if(cunumber < 10){
                txt_CustomerID.setText("C00"+cunumber);
                }else if(cunumber < 100){
                txt_CustomerID.setText("C0"+cunumber);
                }else{
                txt_CustomerID.setText("C"+cunumber);
                }

            }else{
            txt_CustomerID.setText("C001");
            }
    }

    private void generateDate() {
        Date date = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        txt_DATE.setText(sdf.format(date));
    }

    private void calculateSum() {
          DefaultTableModel dtm = (DefaultTableModel) tbl_OrderForm.getModel();

        double sum = 0;
        for (int i = 0; i < dtm.getRowCount(); i++) {
            sum += (double) dtm.getValueAt(i, 4);
        }

        txt_Total.setText(sum +"");
    }

    
