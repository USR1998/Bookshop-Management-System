    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
        int qty = Integer.parseInt(txt_Qty.getText());
        double unit_price =  Double.parseDouble(txt_UnitPrice.getText());
        double price = qty*unit_price;
        DefaultTableModel dtm = (DefaultTableModel) tbl_OrderForm.getModel();
        dtm.addRow(new Object[]{
            txt_BookID.getText(),
            txt_BookName.getText(),
            qty,
            unit_price,
            price
      });
        calculateSum();

        
    }//GEN-LAST:event_btn_AddActionPerformed

    private void btn_RemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_RemoveMouseClicked
        int selectedRow = tbl_OrderForm.getSelectedRow();

        if (selectedRow == -1) {
            return;
        }

        DefaultTableModel dtm = (DefaultTableModel) tbl_OrderForm.getModel();
        dtm.removeRow(selectedRow);

        calculateSum();

    }//GEN-LAST:event_btn_RemoveMouseClicked

    private void btn_PlaceOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_PlaceOrderMouseClicked
            String OID = txt_OrderID.getText();
            String DATE = txt_DATE.getText();
            String CID = txt_CustomerID.getText();
            double TOTAL = Double.parseDouble(txt_Total.getText());              
            ArrayList<ItemDetails> itemDetailList = new ArrayList<>();
            DefaultTableModel dtm = (DefaultTableModel) tbl_OrderForm.getModel();
            for (int i = 0; i < dtm.getRowCount(); i++) {
            String BID = (String) dtm.getValueAt(i, 0);
            String BNAME = (String) dtm.getValueAt(i, 1);
            int QTY = (int) dtm.getValueAt(i, 2);
            double UPRICE = (double) dtm.getValueAt(i, 3);
            ItemDetails itemdetail = new ItemDetails(OID, BID, BNAME, QTY, UPRICE);
            itemDetailList.add(itemdetail);
        }
            Orders order = new Orders(OID, CID, DATE,TOTAL, itemDetailList);
            
        try {
            
            boolean isSaved = OrderController.saveOrder(order);
            if(isSaved){
                    JOptionPane.showMessageDialog(this, "Order Saved");
            }else{
                     JOptionPane.showMessageDialog(this, "Tri Again");
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Order_Form.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Order_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
               
            
            txt_Total.setText("");
            
            DefaultTableModel modal = (DefaultTableModel) tbl_OrderForm.getModel();
            
            while(modal.getRowCount()>0){
                for (int y = 0; y < modal.getRowCount(); y++) {
                    modal.removeRow(y);
                }
            }
            
        try {
            generateOrderID();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Order_Form.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Order_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            generateCustID();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Order_Form.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Order_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
             
    }//GEN-LAST:event_btn_PlaceOrderMouseClicked

